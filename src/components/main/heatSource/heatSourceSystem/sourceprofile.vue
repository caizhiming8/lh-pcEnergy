		<h3>市政热源政策规范</h3>
		<h4>第一章　总　则</h4>
		<p class="p1">第一条 为了规范供热用热行为，提高供热服务质量，维护供热用热双方的合法权益，节约能源，促进供热事业发展，保障和改善民生，根据有关法律、行政法规，结合本省实际，制定本条例。</p>
		<p>第二条 在本省行政区域内从事供热规划、建设、经营、使用及相关管理活动，适用本条例。本条例所称供热，是指供热企业依靠稳定热源，通过管网为用户提供生活用热的集中供热行为。</p>
		<p>第三条 发展供热事业应当遵循政府主导、企业经营、保障安全、节能环保的原则。</p>
		<p>第四条 县级以上人民政府应当将供热事业纳入国民经济和社会发展规划，建立完善的供热保障体系和供热管理协调机制，提高供热保障能力。</p>
		<p>第五条 省、设区的市、县（市）住房城乡建设主管部门或者县级以上人民政府确定的供热管理部门（以下统称供热主管部门）负责本行政区域内供热及相关活动的监督管理工作。发展改革、经济和信息化、财政、环境保护、价格、质量技术监督等部门按照职责分工，做好相关供热管理工作。</p>
		<p>第六条 鼓励利用天然气等清洁能源和太阳能、水能、生物质能、地热能等可再生能源发展供热事业，鼓励和扶持安全、高效、节能环保供热新技术、新工艺、新材料、新设备的研究开发和推广使用。设区的市、县（市）人民政府应当制定天然气等清洁能源替代燃煤供热的规划，对清洁能源利用区域、方式、规模和实施措施作出安排。</p>
		<h4>第二章　规划建设</h4>
		<p>第七条 供热主管部门应当依据城市、县城总体规划组织编制本行政区域的供热专项规划，经法定程序批准后实施。城市、县城供热专项规划应当包含新建住宅小区供热设施同步建设的内容，并对既有住宅小区补建供热设施作出安排。经批准的供热专项规划不得擅自变更；确需变更的，应当报原批准机关批准。</p>
		<p>第八条 城市、县城新区建设和旧城区改建，应当按照城市、县城总体规划和供热专项规划要求，配套建设供热设施，或者预留供热设施配套建设用地。预留的供热设施配套建设用地，任何单位和个人不得擅自占用或者改变用途。</p>
		<p>第九条 编制城市、县城供热专项规划，应当按照城乡统筹的原则将供热设施逐步向镇和农村社区延伸。支持有条件的镇和农村社区配套建设供热设施。</p>
		<p>第十条 新建、改建、扩建工程需要接入供热管网的，建设单位在编制建设工程设计方案时，应当就建设项目供热条件征求供热主管部门意见。供热主管部门应当根据供热专项规划及其实施情况提出答复意见，明确工程是否具备供热条件。对具备供热条件的，确定供热方式及供热单位，并提出供热分项设计技术要求。</p>
		<p>第十一条 具备天然气供应条件且气源充足稳定的城市，不得新建燃煤供热锅炉。在供热管网覆盖区域内，不得新建分散燃煤供热锅炉；供热管网覆盖前已建成使用的分散燃煤供热锅炉，应当限期停止使用，并将供热系统接入供热管网或者采用清洁能源供热。</p>
		<p>第十二条 供热工程的勘察、设计、施工、监理，应当由具有相应资质的单位承担，并执行国家和省有关技术标准和规范。</p>
		<p>第十三条 新建民用建筑应当符合建筑节能强制性标准。既有民用建筑接入供热管网应当进行节能改造，并符合既有建筑节能改造标准。实行供热的新建民用建筑和既有民用建筑节能改造时，应当安装供热系统调控装置、用热计量装置和室内温度调控装置，居住建筑应当安装分户用热计量装置。用热计量装置应当依法检定合格。</p>
		<p>第十四条 新建住宅小区内的供热经营设施（包括供热管道、换热系统和用热计量装置），由供热企业负责投资、设计、建设。房地产开发企业应当协调配合供热经营设施的施工，并承担相关管沟、设备用房等土建工程的配套建设。供热经营设施的建设资金，并入城市基础设施配套费，由房地产开发企业按照规定缴纳，专项用于供热经营设施的投资建设。</p>
		<p>第十五条 供热工程竣工后，供热企业、房地产开发企业等建设单位应当组织竣工验收，供热主管部门应当对验收进行监督，对符合条件的项目，出具供热工程竣工验收合格证明。房地产开发项目未取得供热工程竣工验收合格证明的，房地产开发主管部门不得办理竣工综合验收备案。</p>
		<h4>第三章　供热用热</h4>
		<p>第十六条 供热企业应当实行热源、管网、换热站经营管理一体化。物业服务企业等单位自行管理的住宅小区换热站等供热经营设施应当按照规定限期取消，或者经业主大会同意后向供热企业移交，由供热企业负责统一管理、并网运营。具体办法由设区的市人民政府制定。</p>
		<p>第十七条 供热企业应当具备下列条件，并取得供热主管部门核发的供热经营许可证后，方可从事供热经营活动：</p>
		<p>（一）有可靠、稳定的热源和符合要求的供热设施；</p>
		<p>（二）有与供热规模相适应的资金和经培训具有相应资格的从业人员；</p>
		<p>（三）有规范的经营管理制度、操作规程、服务标准和应急保障措施；</p>
		<p>（四）供热能耗指标和污染物排放指标达到国家和省规定的标准；</p>
		<p>（五）法律、法规规定的其他条件。</p>
		<p>第十八条 供热企业按照供热经营规模实行分级许可，具体办法由省住房城乡建设主管部门制定。供热企业应当按照供热经营许可证的规定从事供热经营活动。供热经营许可证的有效期为五年。</p>
		<p>第十九条　县级以上人民政府可以根据国家和省有关规定，采取招标投标方式确定符合条件的供热企业，并与其签订供热特许经营协议，准予其在一定范围和期限内的供热特许经营权。</p>
		<p>第二十条 供热用热双方应当依法签订供用热合同。供用热合同的主要内容包括供热面积、供热时间、供热质量、收费标准、交费时间、结算方式、供热设施维护责任、违约责任以及当事人约定的其他事项。对已具备供热条件的住宅小区，申请用热户数达到一定数量的，供热企业应当供热。具体办法由设区的市、县（市）人民政府制定。</p>
		<p>第二十一条 设区的市、县（市）人民政府应当确定当地采暖供热期，明确供热期起止日期，向社会公布，并根据气象情况适时调整供热期限。供热企业不得延迟或者提前结束供热。</p>
		<p>第二十二条 在室外温度不低于供热系统最低设计温度、建筑围护结构符合当时采暖设计规范标准和室内采暖系统正常运行条件下，供热企业应当保证采暖供热期内用户卧室、起居室的温度不低于十八摄氏度。供用热合同另有约定的，从其约定。用户认为室内温度不达标的，可以向供热企业提出温度检测要求，供热企业应当在二十四小时内进行检测。对检测结果有异议的，可以委托法定的检测机构进行检测。因供热企业原因导致室内温度不达标的，供热企业应当承担检测费用并减收热费，具体办法由设区的市、县（市）人民政府制定。</p>
		<p>第二十三条 居民采暖热价的制定和调整应当遵循合理补偿成本、促进节约用热、坚持公平负担的原则，并采取听证会的形式征求用户、供热企业和供热主管部门等方面的意见。</p>
		<p>第二十四条 用户具备分户用热计量条件的，供热企业应当按照用热量收费。收费按照基本热价和计量热价相结合的两部制热价核算，按照供热面积核算的基本热价不得超过全部按照供热面积核算热价的百分之三十。用户不具备分户用热计量条件的，按照供热面积收费。设区的市、县（市）供热计量整体改造完成前，对居民用户按照用热量收费数额超过按照供热面积收费数额的，其超过部分的收费上限，由设区的市、县（市）人民政府确定。</p>
		<p>第二十五条 供热企业应当向最终用户收取热费。用户应当按照供用热合同的约定及时足额交纳热费。供热企业可以自行向用户收取热费，也可以委托金融机构或者其他单位代收；用户选择向供热企业直接交纳热费的，供热企业不得拒绝。供热企业和受委托的收费单位应当向用户出具供热企业统一专用发票。受委托的收费单位不得向用户收取任何额外费用。</p>
		<p>第二十六条 供热设施具备分户关闭条件，用户要求暂停供热的，应当在当年采暖供热期开始三十日前向供热企业提出，办理暂停供热手续。用户要求暂停或者恢复供热的，供热企业不得收取任何费用。供热企业不得因部分用户欠交热费，停止向其他已交费用户供热或者降低供热标准。</p>
		<p>第二十七条 县级以上人民政府应当建立供热政策性补贴资金，专项用于补贴供热企业成本与价格倒挂亏损、延长采暖供热期限、供热系统节能和环保改造、旧住宅区供热经营设施改造等。</p>
		<p>第二十八条 对城镇低收入困难家庭和其他需要特殊照顾的家庭，实行政府采暖热费补贴。具体办法由设区的市、县（市）人民政府制定。</p>
		<p>第二十九条 供热企业应当实行标准化管理和规范化服务，向社会公示服务内容、服务标准和办事程序，公开收费标准和服务电话，并在采暖供热期实行二十四小时值班。用户有权就供热收费、供热服务等事项向供热企业查询、投诉，供热企业应当在三日内予以答复。供热企业的工作人员上门服务时，应当佩带统一标志，文明服务，用户应当予以配合。</p>
		<p>第三十条 供热企业应当按照供用热合同约定，连续稳定供热，不得擅自中断或者停止供热。在采暖供热期内，因特殊原因需要连续停止供热超过二十四小时的，供热企业应当提前二日通知用户；因突发事故不能正常供热的，供热企业应当立即组织抢修并报告供热主管部门，及时通知受影响区域的用户。连续停止供热二十四小时以上的，供热企业应当依据停供时间减收相应热费。</p>
		<p>第三十一条 未经供热主管部门批准，供热企业不得擅自停业。确需停业的，应当在当年采暖供热期开始六个月前向供热主管部门提出申请，供热主管部门应当自收到申请之日起二十日内作出是否批准的决定。经批准停业的供热企业应当对供热范围内相关用户、设施管理以及热费等事宜作出妥善安排，在当年采暖供热期开始三个月前与承接的供热企业完成交接，并向供热主管部门提出书面报告。</p>
		<p>第三十二条 用户应当妥善使用和维护自有供热设施，不得有下列妨碍供热设施正常运行的行为：</p>
		<p>（一）擅自在室内供热设施上安装放水阀、排气阀或者换热装置；</p>
		<p>（二）擅自改动供热管道、安装管道泵、增设散热器或者改变用热性质和方式；</p>
		<p>（三）擅自排放供热系统的热水；</p>
		<p>（四）其他妨碍供热设施正常运行的行为。</p>
		<p>第三十三条 用户有权就供热收费、供热质量和供热服务等事项，向价格主管部门、供热主管部门以及其他有关部门投诉。供热主管部门应当对供热企业供热质量和服务质量进行监督检查和考核，公开投诉电话、信箱等，并及时处理用户投诉。</p>
		<h4>第四章　设施管理</h4>
		<p>第三十四条 用热分户计量装置或者入户端口以外的供热经营设施的维修、养护、更新责任，由供热企业承担。用户自有供热设施的养护、维修、更新责任，由用户承担。</p>
		<p>第三十五条 供热企业应当对其负责管理的供热设施进行定期检查和维护，保证其在使用期内安全稳定运行。</p>
		<p>第三十六条 供热企业应当建立健全安全生产责任制。对高温高压等重要供热设施，应当按照规定设置明显的安全警示标志。</p>
		<p>第三十七条 供热主管部门应当建立并实施供热系统能耗统计、监测和考核评价制度。供热企业应当加强供热设施节能减排管理，实施系统节能改造，降低能源消耗，减少污染物排放，逐步建设供热计量温控一体化远程智能调控技术平台，实现热源、热网、换热站、用户能耗在线监测和自动调节。</p>
		<p>第三十八条 建设单位在建设工程开工前，应当向城建档案管理机构或者供热企业查明有关地下供热管线的情况。城建档案管理机构或者供热企业应当及时提供相关资料。建设工程施工可能危害供热设施安全的，建设单位应当与供热企业协商制定安全保护施工方案，并采取相应的安全保护措施后方可施工。在施工中造成供热设施损坏的，应当立即通知供热企业修复，并赔偿损失。</p>
		<p>第三十九条 供热企业应当制定供热事故抢险抢修应急预案，并定期进行演练。供热企业发现供热事故或者接到供热事故报告后，应当立即组织抢险抢修，并同时报告供热主管部门。对影响抢修的其他设施，供热企业应当采取合理的应急处置和必要的现场防护措施，并及时通知有关单位。供热企业可以先行组织施工，有关部门应当允许施工企业事后补办占道、道路开挖等审批手续。用户自有供热设施发生泄漏等紧急情况，给其他用户正常供热造成影响，供热企业需要入户抢修作业的，相关用户、物业服务企业等应当予以配合。</p>
		<p>第四十条 因工程建设确需改建、迁移、拆除供热设施的，建设单位应当与供热企业协商确定改建、迁移、拆除方案后方可实施。</p>
		<p>第四十一条 任何单位和个人不得实施下列危害供热设施安全的行为：</p>
		<p>（一）破坏或者擅自改装、拆除供热管网、标志、井盖、阀门和仪表等供热设施；</p>
		<p>（二）破坏或者擅自安装、拆卸、改装、干扰用热计量设施；</p>
		<p>（三）利用供热管道或者支架悬挂物体；</p>
		<p>（四）在规定的供热设施安全间距范围内，建设建筑物、构筑物、敷设管线或者种植深根植物；</p>
		<p>（五）在规定的供热设施安全间距范围内，爆破、挖坑、掘土或者打桩；</p>
		<p>（六）在规定的供热设施安全间距范围内，堆放垃圾、杂物、易燃易爆等危险物品，排放污水、腐蚀性液体或者气体；</p>
		<p>（七）其他危害供热设施安全的行为。</p>
		<h4>第五章　法律责任</h4>
		<p>第四十二条 违反本条例规定的行为，法律、行政法规已规定法律责任的，按照其规定执行；法律、行政法规未规定法律责任的，依照本条例规定执行。</p>
		<p>第四十三条 违反本条例规定，供热企业、房地产开发企业等建设单位未按照规定安装供热系统调控装置、用热计量装置和室内温度调控装置的，由住房城乡建设主管部门责令改正，处十万元以上三十万元以下罚款。</p>
		<p>第四十四条 违反本条例规定，供热企业、房地产开发企业等建设单位未组织供热工程竣工验收的，由供热主管部门责令改正，处三万元以上五万元以下罚款。</p>
		<p>第四十五条 违反本条例规定，建设单位擅自施工危害供热设施安全的，由供热主管部门责令改正，逾期不改正的，处一万元以上五万元以下罚款；造成损失的，依法承担赔偿责任。</p>
		<p>第四十六条 违反本条例规定，擅自改建、迁移、拆除供热设施的，由供热主管部门责令限期改正，恢复原状或者采取其他补救措施，处五万元以上十万元以下罚款；造成损失的，依法承担赔偿责任。</p>
		<p>第四十七条 违反本条例规定，未取得供热经营许可证从事供热经营活动的，由供热主管部门责令停止违法行为，没收违法所得，处五万元以上五十万元以下罚款。</p>
		<p>第四十八条 违反本条例规定，供热企业不按照供热经营许可证的规定从事供热经营活动的，由供热主管部门责令限期改正，没收违法所得，处三万元以上二十万元以下罚款；情节严重的，吊销供热经营许可证。</p>
		<p>第四十九条 违反本条例规定，供热企业延迟供热、提前结束供热或者拒绝用户直接交纳热费的，由供热主管部门责令限期改正；逾期不改正的，处一万元以上五万元以下罚款。</p>
		<p>第五十条 违反本条例规定，供热企业对具备分户用热计量条件的用户不按照用热量收费的，由供热主管部门给予警告，责令限期改正;情节严重的，吊销供热经营许可证。</p>
		<p>第五十一条 违反本条例规定，供热企业擅自中断或者停止供热、停业的，由供热主管部门给予警告，责令限期改正，并处十万元以上五十万元以下罚款；情节严重的，吊销供热经营许可证；造成损失的，依法承担赔偿责任。</p>
		<p>第五十二条 违反本条例规定，用户有妨碍供热设施正常运行行为的，由供热主管部门责令限期改正，逾期不改正的，对单位用户可以处一万元以上五万元以下罚款，对个人用户可以处二百元以上一千元以下罚款；造成损失的，依法承担赔偿责任。构成违反治安管理行为的，由公安机关依法处罚；构成犯罪的，依法追究刑事责任。</p>
		<p>第五十三条 违反本条例规定，危害供热设施安全的，由供热主管部门责令停止违法行为，限期恢复原状或者采取其他补救措施，对单位处三万元以上十万元以下罚款，对个人处五千元以下罚款；造成损失的，依法承担赔偿责任。构成违反治安管理行为的，由公安机关依法处罚；构成犯罪的，依法追究刑事责任。</p>
		<p>第五十四条 违反本条例规定，供热主管部门和其他有关部门有下列行为之一的，由本级人民政府、上级主管部门依据职权责令改正，通报批评；对直接负责的主管人员和其他直接责任人员依法给予处分；构成犯罪的，依法追究刑事责任：</p>
		<p>（一）未按照规定编制或者擅自变更供热专项规划；</p>
		<p>（二）未按照规定的权限、程序和条件审批供热经营许可；</p>
		<p>（三）对不符合条件的供热工程项目出具竣工验收合格证明；</p>
		<p>（四）给未取得供热工程竣工验收合格证明的房地产开发项目办理竣工综合验收备案；</p>
		<p>（五）未按照规定履行供热服务质量监督管理职责；</p>
		<p>（六）其他玩忽职守、滥用职权、徇私舞弊的行为。</p>
		<h4>第六章　 附　则</h4>
		<p>第五十五条 供热企业提供生产用热的，其经营许可管理适用本条例的有关规定，供用热双方的权利义务通过供用热合同约定。</p>
		<p>第五十六条 本条例自2014年9月1日起施行。</p>
		<h3>第一章　总　　则</h3>
		<p>第一条 为加强城市集中供热管理,节约能源,保护环境,适应生产和人民生活的需要,根据法律、法规的有关规定,结合本市实际,制定本条例。</p>
		<p>第二条　本条例适用于本市市区内的城市集中供热管理。</p>
		<p>第三条　本条例所称城市集中供热是指利用热电联产、区域锅炉、工业余热等热源通过热网向若干个街区乃至整个城市的热用户供热。</p>
		<p>第四条　济南市供热管理部门主管本市城市集中供热行政管理工作(以下称供热主管部门)。市人民政府有关部门应当按照各自的职责,协同供热主管部门做好城市集中供热管理工作。 </p>
		<p>第五条　城市集中供热设施建设应当纳入国民经济和社会发展计划,统筹规划,配套建设,多家经营,统一管理,协调发展。</p>
		<p>第六条　热电联产的总热效率和热电比应当符合国家规定指标,经济综合部门应当加强热电联产电力管理,提高热电机组利用率,保障热电机组安全经济运行。</p>
		<p>第七条　鼓励城市集中供热新技术、新工艺、新设备、新材料的研究、开发和应用,限制、改造分散供热燃煤锅炉,推广热、电、冷联供。</p>
		<p>第八条　鼓励国内外投资者依法建设城市集中供热设施和从事城市集中供热的生产经营活动。</p>
		<h4>第二章　规划与建设</h4>
		<p>第九条　规划行政主管部门应当会同供热主管部门依据城市总体规划编制城市集中供热规划,报经市人民政府批准后,由供热主管部门组织实施。 城市集中供热规划经批准后,任何单位和个人不得擅自变更;确需变更的,应当报原批准机关审批。城市集中供热规划,应当符合城市总体规划确定的城市集中供热发展目标和总体布局,根据城市发展需要合理配置热源、热网,统筹安排,分期实施。</p>
		<p>第十条　新建、扩建、改建城市道路应当依据城市集中供热规划同时设计和敷设供热管网,其建设资金由市人民政府负责筹集。</p>
		<p>第十一条　在城市供热主管网到达的地区进行房地产开发的单位,应当配套建设用热设施,并代热用户向供热主管部门缴纳城市供热工程建设资金。已缴纳的城市供热工程建设资金可以纳入开发建设项目总概算。 在城市供热主管网未到达的地区进行房地产开发的单位,应当按照城市集中供热规划配套建设城市供用热设施,城市供用热设施建设资金可以纳入开发建设项目总概算,并免缴城市供热工程建设资金。 配套建设的城市供用热设施,应当与建设项目工程同时设计、同时施工、同时验收并交付使用。 </p>
		<p>第十二条　新建、扩建、改建城市集中供热工程,必须符合城市集中供热规划</p>
		<p>第十三条　在城市供热主管网到达的地区,不得新建分散供热燃煤锅炉;对建成使用的分散供热燃煤锅炉,应当限期停止使用。</p>
		<p>第十四条　城市集中供用热工程的设计、施工、监理,应当符合国家有关技术标准和规范,由供热主管部门会同有关部门通过招标、投标等形式确定具有相应资质的单位承担。</p>
		<p>第十五条　城市集中供用热设施建设和维修所采用的设备、材料、计量器具等,应当符合国家规定的产品质量标准。有关部门应当进行标准化审查,并加强质量监督检查。 </p>
		<p>第十六条　城市集中供热管线按照城市集中供热规划需要穿越单位、厂区或宅、院时,单位和个人应当予以配合。因穿越施工造成设施损坏的,建设单位应当予以修复;无法修复的,应当给予赔偿。 </p>
		<p>第十七条　城市集中供用热设施建设工程竣工,建设单位应当依法组织验收,未经验收或者验收不合格的不得投入使用。</p>
		<p>第十八条　参加城市集中供热的热用户,应当向供热主管部门或供热主管部门委托的供热设施建设单位缴纳城市供热工程建设资金,但已由房地产开发单位代缴城市供热工程建设资金的热用户除外。城市供热工程建设资金的收缴办法和标准由供热主管部门会同建设、物价、财政部门核定,按规定程序报批后执行。</p>
		<h4>第三章　供热管理</h4>
		<p>第十九条 供热单位使用的供热锅炉应当达到国家、省和市规定的大气污染物排放标准。</p>
		<p>第二十条　供热单位未经供热主管部门同意,不得全部或部分停止或终止城市集中供热的生产经营。</p>
		<p>第二十一条　供热单位应当按照特许经营合同约定的供热范围向热用户供热。</p>
		<p>第二十二条　供热单位应当与热用户签订供用热合同。
　　供用热合同的内容应当包括供用热时间、热负荷性质、供用热—348—参数、收费标准、缴费时限、供用热双方的权利义务、违约责任及当事人约定的其他事项。 供热单位和热用户因执行供用热合同发生纠纷,可以协商解决,也可以依法申请仲裁或向人民法院提起诉讼。 </p>
		<p>第二十三条　本市市区采暖期起止时间为当年十一月十五日至次年三月十五日，根据气候变化，市人民政府应当决定提前或者延长供暖时间，并向社会公告。在采暖期内热用户室温不得低于16℃。供热单位供热运行中的供热参数、热用户室温合格率和运行事故率应当符合国家规定的标准。 </p>
		<p>第二十四条　供热单位应当实行规范化服务,将服务的内容、标准、时间向社会公开,接受监督。</p>
		<p>第二十五条　供热单位应当按照供用热合同按时、连续、保质供热。因突发性故障不能保证正常供热时,应当立即组织抢修,并通知热用户。发生重大故障,应当同时报告供热主管部门。 因供热单位责任停止向热用户供热,供热单位应当按照停止供热的时间减收用热费。
</p>
		<h4>第四章　用热管理</h4>
		<p>第二十六条　需要用热的单位和居民,应当向供热单位申请办理用热手续。散户居民应当以楼单元为单位提出申请。</p>
		<p>第二十七条　热用户更名、增加或减少用热面积,应当到供热单位办理变更手续。</p>
		<p>第二十八条　热用户应当按规定对用热设施采取防寒保护措施;每年用热前,对用热设施进行检修,保证用热设施完好。</p>
		<p>第二十九条　热用户未经供热单位同意,不得有下列行为:</p>
		<p> (一)改变用热管网管径、增加用热管线或散热器; </p>
		<p> (二)在用热设施上安装放水、排汽装置; </p>
		<p> (三)安装或启闭控制装置; </p>
		<p>  (四)转供热、改变用热性质及运行方式; </p>
		<p> (五)排放或取用供热管网蒸汽和热水。 </p>
		<p>第三十条　热用户必须严格执行用热计划,并按照物价部门核定的采暖价格和蒸汽价格按时向供热单位缴纳用热费;逾期不缴纳的,按日缴纳千分之三的滞纳金。</p>
		<p>第三十一条　热用户的采暖价格和蒸汽价格按照“成本+税金+微利”的原则实行分类定价,由物价部门依据《中华人民共和国价格法》的有关规定核定并公布执行。</p>
		<h4>第五章　设施管理</h4>
		<p>第三十二条　城市集中供热设施和用热设施的界限以入户总仪表为准,入户总仪表及其以外的设施为供热设施,由供热单位负责维护管理;入户总仪表以内的设施为用热设施,由房屋产权人负责维护管理。房屋产权人或热用户可以委托供热单位维修用热设施。 </p>
		<p>第三十三条　热计量器具应当经技术监督部门检验合格后,方可安装使用。供用热双方对计量发生争议时,可申请技术监督部门检测鉴定,检测费用由责任方承担。</p>
		<p>第三十四条　供热单位应当按照规定在其负责维修管理的城市集中供热设施及其安全距离范围内设置明显的安全警示标志。</p>
		<p>第三十五条　任何单位和个人不得有危害城市集中供用热设施安全运行的行为。 供热单位对其管理和受委托管理的城市集中供用热设施应当定期巡线检查维修,确保供用热设施安全正常运行。供热管理人员入户巡检用热设施时,应当出示由供热主管部门颁发的证件,热用户应当予以配合。 </p>
		<p>第三十六条　城市热网及其附属设施发生故障需要抢修时,供热单位可以先施工,并在施工期限内到有关部门补办有关手续。</p>
		<p>第三十七条　任何单位和个人不得擅自改装、拆除、迁移城市集中供用热设施。因特殊情况确需改装、拆除、迁移的,须经供热主管部门同意后方可施工,因此发生的有关费用由责任方承担。 </p>
		<p>第三十八条　规划行政主管部门在审批涉及城市集中供热设施安全的项目时,应当事先征求供热主管部门的意见。因工程施工影响城市集中供热设施安全的,建设单位应当采取安全保护措施,经供热单位查验同意后方可施工。</p>
		<p>第三十九条　未经供热单位同意,任何单位和个人不得将用热管网与供热管网连接。</p>
		<p>第四十条　城市热网及其附属设施外缘安全距离范围内,禁止下列行为: </p>
		<p>　(一)修建建(构)筑物; </p>
		<p>　(二)挖坑、掘土、打桩、埋设线杆; </p>
		<p>　(三)爆破作业; </p>
		<p>　(四)堆放垃圾、杂物; </p>
		<p>　(五)排放污水。</p>
		<h4>第六章　法律责任</h4>
		<p>第四十一条　有下列行为之一的,由供热主管部门责令限期改正,并处以五千元以上五万元以下罚款: </p>
		<p>	　(一)新建或者启用已禁止使用的分散供热燃煤锅炉的; </p>
		<p>　(二)应当建设集中供用热设施而未建的。</p>
		<p>第四十二条　供热单位有下列行为之一的,由供热主管部门责令限期改正,并处以三千元以上三万元以下罚款;有违法所得的,没收违法所得: </p>
		<p>　(一)供热运行中供热参数、热用户室温合格率和运行事故率不符合国家规定标准的; </p>
		<p>　(二)未按规定时间供热或擅自缩短供热期的; </p>
		<p>　(三)发生重大故障未向供热主管部门报告的; </p>
		<p>　(四)热用户室内温度未达到规定标准的。</p>
		<p>第四十三条　违反本条例第二十条规定的,由供热主管部门责令限期恢复生产经营;逾期仍不恢复的,由供热主管部门委托具有相应资质的供热单位管理,并处以五万元以上十万元以下罚款。 </p>
		<p>第四十四条　违反本条例第二十九条规定的,应当恢复原状,造成损失的应当予以赔偿。</p>
		<p>第四十五条　危害城市集中供热设施安全,有下列行为之一的,由供热主管部门责令限期改正,并处以三千元以上三万元以下罚款: </p>
		<p>　(一)擅自改装、拆除、迁移城市集中供用热设施的;</p>
		<p>　(二)工程施工影响城市集中供热设施安全,未采取安全保护措施擅自施工的; </p>
		<p>　(三)擅自将用热管网与供热管网连接的;</p>
		<p>　(四)违反本条例第四十条规定的。</p>
		<p>第四十六条　城市供热行政管理人员玩忽职守、滥用职权、徇私舞弊的,由其所在单位或上级主管部门给予行政处分;构成犯罪的,依法追究刑事责任。</p>
		<p>第四十七条　依照本条例实施处罚时,必须使用财政部门统一制发的罚款收据,罚款收入按规定上缴财政。</p>
		<p>第四十八条　对拒绝、阻碍供热主管部门的行政执法人员执行公务,依照《中华人民共和国治安管理处罚法》应当予以处罚的,由公安机关依法处理;构成犯罪的,依法追究刑事责任。</p>
		<p>第四十九条　当事人对行政处罚决定不服的,可以依法申请行政复议或者提起行政诉讼。逾期不申请复议也不向人民法院起诉又不履行处罚决定的,由作出处罚决定的机关申请人民法院强制执行。</p>
		<h4>第七章　附　　则</h4>
		<p>第五十条　本市市区内的生活热水供应和集中供冷及县(市)城市集中供热管理参照本条例执行。</p>
		<p>第五十一条　市人民政府应当依照本条例制定实施细则。第五十二条　本条例自公布之日起施行。</p>
