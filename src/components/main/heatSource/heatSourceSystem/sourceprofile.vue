		<h4>第二章 技术标准</h4>
		<p>2.1 设计标准</p>
		<p>　2.1.1 热力站(房)供回水温度、压力、流量等运行设计参数须符合《城市热力网设计规范》的规定要求。</p>
		<p>　2.1.2热力网供回水流量等设计参数须符合《城市热力网设计规范》的规定要求。</p>
		<p>　2.1.3热力网供回水压力等设计参数须符合《城市热力网设计规范》的规定要求，保证热网末端供回水压差能满足用户系统所需的资用压头要求。</p>
		<p>　2.1.4供热循环水、补给水水质须符合国家相关水质标准。</p>
		<p>2.2 运行标准</p>
		<p>　2.2.1热力站(房)配置检测和调节供回水温度、压力、流量等运行参数的设施，实施检测与调节，保证良好的水力供热介质工况。</p>
		<p>　2.2.2热力网供回水温度、压力、流量等参数在供热协议中要明确规定，并按供热协议要求实施，保证用户采暖效果;供热中蒸汽的压力、温度、流量标准，应由供用双方协商后在供热合同中约定并执行。</p>
		<p>2.3 计量器具标准</p>
		<p>　供热单位应使用法定计量检定机构检验合格的计量器具定期测量供、用设施界限点的运行参数和用户室内温度。</p>
		<h4>第三章 服务标准</h4>
		<p>3.1 发展用户服务</p>
		<p>　3.1.1需要申请用热的单位和居民，应向供热单位申请办理用热手续。散户居民应以整楼、整单元为单位提出申请。</p>
		<p>　3.1.2 开户基本程序:申请用热户向供热单位提出开户申请——供热单位受理——市政公用事业局审批——政务审批中心交费——与供热单位签订开户协议——供热办备案。</p>
		<p>　3.1.3供热单位必须与热用户签订集中供热开户协议与供用热合同</p>
		<p>　3.1.4对符合供热条件的用户，一般情况下，供热单位在15日内与热用户签订开户协议。</p>
		<p>3.2 供热服务</p>
		<p>　3.2.1 集中供热时间为当年11月15日零时至次年3月15日零时，公休、节假日不间断供热。</p>
		<p>　3.2.2集中供热实施供热调试期制度，每年11月8日至11月14日为供热调试期(遇市政府紧急调度以政府确定时间为准)。</p>
		<p>3.2.4采暖计费逐步实行按热量计费。</p>
		<p>3.2.5供热单位须按市物价部门批准的价格收缴热费。</p>
		<p>3.2.6供热单位不得因个别用户不交费而牵连整单元、整栋楼或整个小区、宿舍用热。</p>
		<p>3.2.7 对济南市持有《城市居民最低生活保障证》或《济南市特困职工优待证》的居民家庭，供热单位按照物价部门规定的优惠价格收取热费。</p>
		<p>3.3 安全服务</p>
		<p>3.3.1供热单位须按《中华人民共和国安全生产法》及有关安全规程制定安全技术操作规程及相关的安全制度，并认真组织实施，保障供热系统的安全。</p>
		<p>3.3.2供热单位在用的特种设备、计量器具及其他安全设施须按国家有关规定进行测定和检验。</p>
		<p>3.3.3供热单位应对生产岗位工作人员进行技术培训，其中司炉工、水质化验人员、焊工、电工等须取得相应资格证和上岗证。</p>
		<p>3.3.4供热单位应按规定在必要的地方设置安全警示标志。</p>
		<p>3.3.5 供热单位必须制订针对极端天气、运行事故等的应急预案，并定期培训和演练。</p>
		<p>3.3.6供热单位应加强集中供热安全知识的宣传、普及工作，提高全民集中供热安全意识，积极防范各种集中供热事故的发生。</p>
		<p>3.4 设施抢修维修服务</p>
		<p>3.4.1供热单位应定期对其管理和受托管理的城市集中供热设施进行巡线检查维修，发现“跑、冒、滴、漏”或接到相应报告后，及时修复。</p>
		<p>3.4.2热用户可委托供热单位维修用热设施。</p>
		<p>3.4.3供热期间设备发生故障，一般故障在8小时内修复。</p>
		<p>3.4.4供热单位接到供热井盖破损、丢失报告后，2小时内到达现场，设置警示标志，并及时补装更换;对需要大修井口的，一般在48小时内完成。</p>
		<p>3.4.5供热单位的热力站(房)必须准确记录停热和恢复供热的时间、原因和处理措施等，运行事故在24小时内上报市供热行政主管部门。</p>
		<p>3.4.7供热单位的设备完好率在99%以上。</p>
		<p>3.4.8供热单位的供热系统运行事故率低于1%。</p>
		<p>3.4.9供热单位的年度设备检修在非采暖期进行，并兼顾常年用户的需求。若遇特殊情况，须服从供热主管部门的安排，供热单位不得擅自对已供热用户终止供热。</p>
		<p>3.5 测温服务</p>
		<p>3.5.1采暖期内供热单位须按《城镇供热系统安全运行技术规程》的规定设立用户室温监测点，对用户室温进行检测，并在供热辖区末端、瓶颈点或投诉集中区域、楼前阀位置(或产权界定点)设立压力、温度等参数监测点，以便及时分析温度不达标的原因，处理供热纠纷。</p>
		<p>3.5.2按面积缴纳采暖费的用户认为室内温度不达标时，可以向供热单位提出测温申请，供热单位在5天内对用户的测温申请做出处理，并进行免费室温检测服务。</p>
		<p>3.5.3用户对供热单位的测温结果有异议的，可以委托第三方检测机构进行室温检测。</p>
		<p>3.5.4经检测，用户室内温度不合格且属供热单位原因造成的，供热单位须向用户退还采暖费，并立即采取有效措施，尽快使用户温度达到规定标准。非供热单位原因造成供热温度不合格的，不属于退还采暖费范围。</p>
		<p>3.5.5室温检测一次不合格的，视为检测当日及第二天室温不合格。用户室温不合格，供热单位按照下列标准向用户退还采暖费:室温在14-18 ℃(含14℃)的，退还不合格天数采暖费的50%;室温低于14℃的，退还不合格天数采暖费的100%。具体室温检测与退费程序按照政府相关文件执行。</p>
		<p>3.6 关联服务</p>
		<p>3.6.1 热用户变更</p>
		<p>3.6.1.1热用户需要更名过户，应向供热单位申请办理，供热单位须给予方便快捷的业务办理。</p>
		<p>3.6.1.2热用户变更的材料准备:用热面积没有发生变化的，新用户需持双方同意的更名过户协议书到供热单位办理更名过户手续。用热面积发生改变的，办理变更手续同时需要增加用热面积的，应当缴纳增容部分供热管网建设费;需要减少用热面积的，已缴纳的供热管网建设费不予退还。</p>
		<p>3.6.2 用热报停</p>
		<p>3.6.2.1具备分户控制的单户或整单元、整幢楼的用户可申请停止用热，供热单位应书面报停、电话报停和短信报停等多种方式。</p>
		<p>3.6.2.2供热单位要及时受理热用户的停止用热申请，审核通过后协助办理停热手续。</p>
		<p>3.6.2.3具备分户控制的居民用户，在办理停热手续时向供热单位缴纳热网运行成本费，征收标准不得超过每户总热费的15%。</p>
		<p>3.6.3 告知服务</p>
		<p>3.6.3.1供热期开始前，供热单位提前3天告知用户充水时间;供热期内因检修等有计划停热，提前3天告知用户;如遇停水、停电等非供热单位原因造成供热中断，供热单位应提前或及时通知用户，并在水、电等恢复供应后尽快恢复供热。</p>
		<p>3.6.3.2 供热期间设备发生故障后，及时通知热用户。因突发事故不能提前告知的，要在组织抢修的同时通知用户，并报告市供热行政主管部门和有关部门。</p>
		<p>3.6.3.3供热单位的年度设备检修提前15日通知用户，同时报告市供热行政主管部门。</p>
		<p>3.6.3.4供热单位向热用户提供上述告知服务应将《供热片区便民服务卡》及通知张贴到楼单元或小区公告栏，方便用户获知相关信息。</p>
		<p>3.6.3.5供热片区便民服务卡使用市政公用事业局监制的标准样式，写明供热单位名称和地址、供热片区负责人姓名和电话、供热客服经理姓名和电话、热线服务电话、监督电话等内容。</p>
		<p>3.7 从业人员服务标准</p>
		<p>3.7.1  客户服务中心 供热企业建立一站式客户服务中心，设有专门的客户来访引导员，为客户提供业务办理引导服务。供热企业须将用户办理 各种业务所需的资料以及用户关心的标准和有偿服务收费标准编印成宣传页，连同所需填写的各种表格摆放在服务网点适当位置，有条件的应将相关信息公示在企业网站上供用户下载。</p>
		<p>3.7.2  上门服务 服务人员上门服务时，应礼貌敲门，并主动出示相关证件，向用户说明来由，征得用户同意后方可入户服务。入户前要穿着鞋套。使用“请您”、“您好”、“请原谅”、“打扰您”、“谢谢您的合作”、“再见”等文明服务用语。安装、维修人员完成相关服务后，主动清洁服务现场，并请用户填写用户意见反馈卡。服务完成12小时内(夜间9点至早上8点除外)对客户进行电话回访，并征求客户对服务人员的意见。 服务过程中，服务人员须秉公办事，不得吃、拿、卡、要，不得刁难用户。</p>
		<p>3.7.3  着装</p>
		<p>企业入户服务人员着统一工装，衣着整洁，左胸位置烫印“市政公用”字样，佩带胸卡，仪表端庄，举止文明，使用文明服务用语。佩戴胸卡符合下列要求:(一)第一行印“济南市政公用”;(二)第二行印“供热企业名称”;(三)持证人员的姓名或工号;(四)持证人员的照片;(五)监督投诉电话;(六)发证单位名称及公章。</p>
		<p>3.7.4  接待</p>
		<p>企业直接与用户接触的窗口服务人员、客服人员、抄表人员、维修人员应经培训后持证上岗。业务人员须具有职业所必需的上岗资格证书。</p>
		<p>服务窗口人员在企业营业时间内和上门服务时，身着整洁的企业标识服，配戴有企业统一标志的工作牌。具有良好的职业道德意识、熟练的业务技能，接待用户时，主动、礼貌、热情，不推诿、搪塞。客服电话接听人员在电话铃响3声内接听来电;不得无故中途挂断用户电话;电话交流完毕应礼貌道“再见”。</p>
		<p>3.7.5  投诉处理</p>
		<p>企业受理用户的投诉或其它部门转办的投诉，一般投诉件2日内办结;复杂投诉件7日内办结;对新闻媒体曝光的服务问题，必须于2个工作日内核实情况，并按信息来源渠道向社会和新闻媒体反馈结果。</p>
		<p>局相关职能部门建立群众投诉受理、处置督察机制，定期对企业用户投诉处理情况进行监督检查，对处理不及时、不得当，造成不良后果的，要提出明确整改要求和处理意见。</p>
		<h4>第四章 管理标准</h4>
		<p>4.1 行政审批</p>
		<p>4.1.1集中供热行政主管部门在实施批准或者核准时，必须严格按照规定的条件和程序对有关事项进行审查。</p>
		<p>4.1.2 行政审批时限规定</p>
		<p>(1) 符合条件的集中供热经营许可审批、集中供热供应许可审批自受理申请之日起30日内，作出批准或者核准的决定。</p>
		<p>(2)符合条件的改装、迁移、拆除集中供热设施审批，集中供热经营企业歇业、停业审批，新建、改建、扩建集中供热工程项目审核自受理申请之日起15日内，作出批准或者核准的决定。</p>
		<p>4.1.3集中供热行政主管部门在实施批准或者核准时，程序公开，并依法接受监督。</p>
		<p>4.1.4集中供热经营许可证和集中供热供应许可证实行年检制度。未经检验或经检验不合格的，不得继续从事集中供热经营活动。</p>
		<p>4.2 特许授权</p>
		<p>4.2.1市市政公用事业局是济南市集中供热行业的行政主管部门，经市人民政府授权，负责集中供热经营企业特许经营的组织实施。市集中供热管理办公室负责集中供热经营企业特许经营的日常监管。</p>
		<p>4.2.2监管部门须制定城市供热行业发展规划和年度发展计划，报市政府批准后与城市建设同步实施。</p>
		<p>4.2.3监管部门按照特许经营办法规定的程序依法确定城市供热特许经营者，特许经营者应在10个工作日内与监管部门签订《济南市市政公用行业特许经营者从业承诺书》(以下简称承诺书)，监管部门同时向特许经营者颁发《济南市市政公用行业特许经营授权书》(以下简称授权书)。</p>
		<p>4.2.4现供热单位申请供热特许经营应当提供下列资料:</p>
		<p>(1)供热单位特许经营申请表，一式二份;</p>
		<p>(2)企业法人(单位)资格证书(复印件);</p>
		<p>(3)建设项目批准文件，压力容器合格证、安全及消防设施资料;</p>
		<p>(4)工商行政部门颁发的供热经营登记证;</p>
		<p>(5)热源情况，外购热源的企业应当提交供需协议书;</p>
		<p>(6)用热单位合同明细表及供热区域示意图(1:2000-1:20000);</p>
		<p>(7)环保部门的环境评估批复意见;</p>
		<p>(8)企业经营章程，服务规范;</p>
		<p>(9)供热特许经营者承诺报告。</p>
		<p>4.2.5监管部门建立供热单位质量信誉等级评价制度，评价结果作为行业特许经营的重要依据。</p>
		<p>4.2.6监管部门须建立并实施供热生产经营保证金制度，以保障特许经营者持续、安全、优质供热。</p>
		<p>4.2.7取得特许经营权的企业，因破产、特许经营权中止或其他突发事件，不能继续履行特许经营协议的，当危及或者可能危及公众利益、公共安全时，由集中供热行政主管部门和监管部门按照特许经营协议的规定采取应急措施。</p>
		<p>4.3 安全管理</p>
		<p>4.3.1集中供热行政主管部门须加强集中供热安全知识的宣传、普及工作，提高全民集中供热安全意识，积极防范各种集中供热事故的发生。</p>
		<p>4.3.2集中供热行政主管部门应当对集中供热的工程建设、经营、使用、设施保护、器具安装维修等活动进行监督检查;发现安全隐患的，及时通知有关单位和个人限期消除。</p>
		<p>4.3.3集中供热行政主管部门和企业必须建立集中供热安全预警联动机制和应急处置机制，保障突发事件快速有效处置。</p>
		<p>4.4 监督考核</p>
		<p>4.4.1集中供热行政主管部门具体负责集中供热行业的日常监管，建立举报和投诉制度，公开举报和投诉电话、信箱或者电子邮件地址，受理有关集中供热安全、收费标准和服务质量的举报和投诉，并健全机构和机制予以督办。</p>
		<p>4.4.2集中供热行政主管部门主动深入用户小区、用热单位，及时发现和解决集中供热供应与服务中出现的问题。问题受到客观因素制约，不能及时解决的问题须向用户说明原因，并创造条件解决。</p>
		<p>4.4.3集中供热行政主管部门应聘请社会监督员参与服务质量监督，征求用户对集中供热服务的意见和要求;每年对服务质量进行群众满意度测评，对不能达到协议约定要求、群众满意度低、监管部门责令整改又拒不整改的，在媒体上公开曝光，并根据奖惩机制进行处罚。</p>
		<p>4.4.4集中供热行政主管部门定期汇总12319、12345等部门反馈的集中供热投诉咨询情况，分析用户投诉咨询的焦点问题和产生原因，研究问题解决方案与处理办法，负责问题的处理与督办。</p>
		<p>4.5 责任管理</p>
		<p>4.5.1集中供热经营企业违反有关规定，由集中供热行政主管部门依照《济南市城市集中供热管理条例实施细则》，协同执法部门依法处理。</p>
		<p>4.5.2集中供热行政主管部门工作人员要认真履行监管职责，及时发现问题，并妥善处理，对玩忽职守造成不良影响的，按照有关规定追究责任。</p>
		<h4>第五章 附 则</h4>
		<p>5.1  术语与指标解释</p>
		<p>5.1.1热用户:从供热系统获得热能的单位或居民用户。</p>
		<p>5.1.2供热建筑面积:采暖建筑物层高(未装饰前的层高)在3米及3米以下的，其建筑面积即为供热建筑面积;层高超过3米的，其供热建筑面积为建筑面积乘以加高系数。</p>
		<p>供热建筑面积=建筑面积×加高系数</p>
		<p>加高系数=实际建筑层高(米)÷3</p>
		<p>5.1.3 套内建筑面积:房屋套内面积由套内使用面积、套内墙体面积、阳台建筑面积三部分组成。</p>
		<p>阳台建筑面积:全封闭的阳台按其外围水平投影面积计算，未封闭的阳台按其围护结构外围水平投影面积的一半计算。</p>
		<p>5.1.4设备完好率=设备实际运行小时数/设备应运行小时数×100%</p>
		<p>5.1.5运行事故:凡供热设施在供热运行中发生故障造成供热系统停运，24小时以内不能恢复，即视为运行事故。</p>
		<p>运行事故率:指事故延续小时和事故面积与报告期供热单位总供热小时和总供热面积之积的比率。</p>
		<p>运行事故率=∑(事故延续小时由事故造成中断的供热面积)/(采暖期总供热小时×总供热面积)</p>
		<p>5.1.6用户报修处理及时率:用户报修及时处理次数与用户报修总次数的比率。及时处理的标准为:自用户提出报修，在24小时内处理完毕并使用户满意(用户签字)的视为及时处理。</p>
		<p>用户报修及时率=用户报修及时处理次数/用户报修总次数×100%</p>
		<p>5.1.7用户室温合格率=检测合格户数/检测总户数×100%</p>
		<p>5.2各集中供热企业的供热服务除执行本标准外，尚需执行国家现行的法律法规和有关行业标准和规定。</p>
		<p>5.3各集中供热企业应以本标准为基础，结合自身实际，制定具体的服务标准实施细则，建立健全自我评价体系和内部管理体系。</p>
		<p>5.4本标准自2015年5月1日起施行。</p>
		<p>5.5 本标准由济南市市政公用事业局负责解释。</p>
		<h3>济南市城市建设综合配套费征收管理办法</h3>
		<p>第一条　为加强城市建设综合配套费的征收和管理,促进我市基础设施建设,根据国家、省有关规定,结合我市实际,制定本办法。</p>
		<p>第二条　城市建设综合配套费是市政府对市区进行各类基础设施配套建设的财政性资金。主要用于开发小区以及单体建设项目规划红线以外的公共配套设施建设。包括:城市建设配套费(道路、桥梁、环境卫生、园林绿化等基础设施的建设)和管网建设费(供水及排水管网、供气管网、热源管网、综合布线管网的建设)。</p>
		<p>第三条　凡在本市市区范围内新建、扩建和改建城市建设项目的,均应交纳城市建设综合配套费。</p>
		<p>第四条　城市建设综合配套费由济南市城市建设基金管理办公室统一征收。</p>
		<p>第五条　济南市城市建设基金管理办公室凭批准的文件到市物价局办理收费许可证,原有关单位的收费许可证中的相关收费项目同时注销。</p>
		<p>第六条　城市建设综合配套费的收费标准按所建设项目的建设面积计收,每平方米246元。其中城市建设配套费92元(路桥82元、环卫5元、绿化5元),管网建设费154元(包含热源78元、供水26元、燃气20元、排水20元、综合布线10元),使用范围见附表。</p>
		<p>第七条　城市建设综合配套费在领取建设工程规划许可证前缴纳,凡未按规定交纳城市建设综合配套费的项目,不得核发规划和施工许可证;违法建设项目经规划主管部门查处后,凡同意保留使用的,须补缴相应配套费,并取得规划确认证明后,房管部门方可予以办理确权手续。</p>
		<p>第八条　城市建设综合配套费的收缴要遵守财政资金管理制度,实行收支两条线和票款分离,专款专用,不得挪作他用,并接受市物价、财政、审计部门的监督检查。</p>
		<p>第九条　下列建设项目经审查可免缴城市建设配套费:</p>
		<p>　(一)驻济大中专院校、中小学、技校和幼儿园的教学设施;</p>
		<p>　(二)驻济部队的军事设施;</p>
		<p>　(三)养老院、社会福利设施、社会公益性设施;</p>
		<p>　(四)享受免收“三税”残疾人员生产、生活设施;</p>
		<p>　(五)法律、法规规定的其他免缴范围。</p>
		<p>第十条　下列建设项目经审查可减缴城市建设配套费:</p>
		<p>　(一)驻济部队、大中专院校、中小学、技校和幼儿园的住宅、学生宿舍、食堂等后勤服务设施减半征收;</p>
		<p>　(二)非营利性公立医疗卫生机构的医疗设施减半征收;</p>
		<p>　(三)法律、法规规定的其他减缴范围。</p>
		<p>第十一条　因特殊情况确需减免的,需先按规定缴费,再由市财政局通过收支两条线的办法经批准后办理。</p>
		<p>第十二条　通过拍卖程序获得土地,拍卖底价中已包含城市建设配套费的,不再交纳城市建设配套费。</p>
		<p>第十三条　城市建设配套费应当一次性缴清。建设开发面积2万平方米以上(含2万平方米)的项目,一次性缴费确有困难的,在缴足应缴费总额50%的前提下,可以对应建设工期分期缴纳,并签订缓缴合同,但最长不超过一年半;未按期交纳城市建设配套费的,除补缴相应规费外,另加收5%的滞纳金。</p>
		<p>第十四条　任何单位和个人均无权减免城市建设配套费。对符合规定应予减、免、缓的,由济南市城市建设项目审批小组审核后作出决定。如水、热、气中某项配套不能实现,可暂缓缴纳相应的配套费,待该配套设施到位后再予以缴纳。对已建成的建设项目需分项配套水、热、气等设施的,需经相关行业部门核准后,据此办法标准单项交费。</p>
		<p>第十五条　已享受减免政策,建成后擅自改变原批准建设用途的建设项目,须补交已减免费用,并加收差额10%的滞纳金。</p>
		<p>第十六条　凡逃交或漏交城市建设综合配套费的,按本办法补交城市建设综合配套费,并加收应缴总额10%的滞纳金。</p>
		<p>第十七条　有下列情况之一的,应追究其相应责任:</p>
		<p>　(一)违反本办法第七条,给未按规定交纳城市建设综合配套费的建设工程核发许可证的,由其上级主管部门责令改正,并对直接责任人员给予行政处分;</p>
		<p>　(二)违反城市建设综合配套费征收管理“收支两条线”规定的;依照国务院《违反行政事业性收费和罚没收入收支两条线管理规定行政处分暂行规定》给予行政处(三)违反本办法第十三条擅自审批城市建设配套费减、免、缓的,责令改正,并对直接责任人员给予相应的行政处分。</p>
		<p>第十八条　当事人对建设行政主管部门依据本办法作出的具体行政行为不服的,可依法申请行政复议或提起行政诉讼。</p>
		<p>第十九条　本办法由市建设委员会、物价局、财政局在各自的职责范围内负责解释。</p>
		<p>第二十条　本办法自颁布之日起施行。过去单项征收的供水、供气、供热集资和其他相关收费政策同时停止执行。过去有关规定与本办法不一致的,一律按本办法执行。</p>
